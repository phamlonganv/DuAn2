  private void LoadBangKhachHang(String ma) {

        try {

            DefaultTableModel model = (DefaultTableModel) tblKhachHangForm.getModel();

            model.setRowCount(0);

            List<KhachHangMD> list = KhachHangDao.getListKhachHangById(ma);

            if (ma.length() == 0) {

                list = KhachHangDao.getListKhachHang();

            }

            for (KhachHangMD list1 : list) {

                Object row[] = {
                    list1.getMaKhachHang(),
                    list1.getTenKhachHang(),
                    list1.getDiaChi(),
                    list1.isGioiTinh(),
                    list1.getCmnd(),
                    list1.getSdt(),
                    list1.getQuocTich(),
                    list1.getTuoi(),
                    list1.isIsDangki()

                };

                model.addRow(row);

            }

        } catch (SQLException ex) {

            Logger.getLogger(Menu.class
                    .getName()).log(Level.SEVERE, null, ex);

        }

    }

