    private boolean isValidE123(String sdt) {
        Pattern p = Pattern.compile("^\\+(?:[0-9] ?){6,14}[0-9]$");

        Matcher m = p.matcher(sdt);
        return (m.find() && m.group().equals(sdt));
    }

